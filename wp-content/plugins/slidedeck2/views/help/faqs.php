<h3>SlideDeck 2 for WordPress FAQs</h3>
<p><strong>Where do I start?</strong><br/>
To see instant results, click on the <em>FeedDeck</em> icon below, then choose <em>RSS</em>. You can also choose <em>VideoDeck</em> and select the <em>YouTube</em> content source. Or choose a category or source below to your liking.</p>
<p><strong>What are some things I can do with SlideDeck 2?</strong><br/>
You can create <em>ImageDecks</em> from your WordPress images or other image sources like <em>Flickr</em>, <em>Instagram</em> or <em>Google+/Picasa</em>. Or create a <em>VideoDeck</em> from your <em>YouTube</em> account or a list of videos.</p>
<p><strong>What are Lenses?</strong><br/>
Lenses are like skins on steroids. They can perform logic, have many options to help you tailor your deck to your specific needs.</p>
<p><strong>What happened to custom content SlideDecks?</strong><br/>
In SlideDeck 2 we focussed more on presenting your existing content in a compelling way. This means less work for you and better looking SlideDecks. We now fully support custom post-types and taxonomies, so you can author your content that way as well.</p>
<p><strong>What happened to my old SlideDecks if I upgraded to SlideDeck 2?</strong><br/>
Nothing, they are still there. SlideDeck 2 runs as a completely independent plugin and will not affect your existing SlideDecks.</p>
<p><strong>If I notice a bug, where do I report it?</strong><br/>
